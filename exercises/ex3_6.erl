-module(ex3_6).
-export([quick/1, merge/1]).

%%----------------------------------------------------------------------
%% API function quick
%%----------------------------------------------------------------------
quick([Pivot|T]) ->
    quick([ X || X <- T, X < Pivot]) ++
    [Pivot] ++
    quick([ X || X <- T, X >= Pivot]);
quick([]) -> [].

