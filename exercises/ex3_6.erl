%%% Quicksort and Merge sort algorithms
%%%
%%%--------------------------------------------------------------------- 
%%% Exports
%%%--------------------------------------------------------------------- 
%%% qsort
%%%   
%%% merge
%%%---------------------------------------------------------------------
-module(ex3_6).
-export([qsort/1, merge/1]).

%%----------------------------------------------------------------------
%% API function qsort
%%----------------------------------------------------------------------
qsort([Pivot|Tail]) ->
    qsort([ X || X <- Tail, X < Pivot])
    ++ [Pivot] ++
    qsort([ X || X <- Tail, X >= Pivot]);
qsort([]) -> [].

%%----------------------------------------------------------------------
%% API function merge
%%----------------------------------------------------------------------
merge(L) -> L.