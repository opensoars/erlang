%%----------------------------------------------------------------------
%% API function ints_to_atom
%%----------------------------------------------------------------------

int_to_atom([]) -> [];
int_to_atom([H | T]) ->
  case H of
    0 -> zero;
    1 -> one;
    2 -> two;
    3 -> three;
    4 -> four;
    5 -> five;
    6 -> six;
    7 -> seven;
    8 -> eight;
    9 -> nine 
  end.