-module(custom).
-export([ints_to_atom/1]).

%%----------------------------------------------------------------------
%% API function ints_to_atom
%%----------------------------------------------------------------------

int_to_atom([]) -> [];
int_to_atom([H | T]) ->
  case H of
    0 -> [zero | int_to_atom()];
    1 -> [one | int_to_atom()];
    2 -> [two | int_to_atom()];
    3 -> [three | int_to_atom()];
    4 -> [four | int_to_atom()];
    5 -> [five | int_to_atom()];
    6 -> [six | int_to_atom()];
    7 -> [seven | int_to_atom()];
    8 -> [eight | int_to_atom()];
    9 -> [nine | int_to_atom()] 
  end.