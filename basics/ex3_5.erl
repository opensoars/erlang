-module(ex3_5).
-export([filter/2, reverse/1]).

%%----------------------------------------------------------------------
%% API function filter
%%----------------------------------------------------------------------
filter(O, N) ->
  filter_acc(O, N, 0, []).


filter_acc(_O, WantLen, CurLen, New) when WantLen == CurLen ->
  reverse(New);
filter_acc([H | T], WantLen, CurLen, New) ->
  filter_acc(T, WantLen, CurLen + 1, [H | New]).


%%----------------------------------------------------------------------
%% API function reverse
%%----------------------------------------------------------------------
reverse(L) ->
  L.



